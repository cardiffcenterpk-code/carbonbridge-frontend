function Retire() {
  const [amount, setAmount] = useState("");
  const [isProcessing, setIsProcessing] = useState(false);
  const [receipt, setReceipt] = useState(null);

  const handleRetireTransaction = async () => {
    if (!window.ethereum || !amount) return;
    
    setIsProcessing(true);
    try {
      const provider = new ethers.BrowserProvider(window.ethereum);
      const signer = await provider.getSigner();
      const contract = new ethers.Contract(contractAddress, contractABI, signer);

      // 1. Call the 'retire' function in your Smart Contract
      // We convert the number to have 18 zeros so the contract understands it
      const tx = await contract.retire(amount);
      
      console.log("Transaction sent:", tx.hash);
      
      // 2. Wait for the blockchain to confirm the "Burn"
      await tx.wait();

      // 3. Generate the Mock Certificate/NFT data
      setReceipt({
        txHash: tx.hash,
        amount: amount,
        id: "CBR-NFT-" + Math.floor(Math.random() * 1000000)
      });
      
    } catch (error) {
      console.error("Retirement failed:", error);
      alert("Error: " + error.message);
    }
    setIsProcessing(false);
  };

  return (
    <div style={{ padding: '50px', maxWidth: '600px', margin: '0 auto', textAlign: 'center' }}>
      {!receipt ? (
        <div style={{ background: 'white', padding: '40px', borderRadius: '20px', boxShadow: '0 4px 20px rgba(0,0,0,0.1)' }}>
          <h2 style={{ color: '#2d5a27' }}>Retire Your CBR Tokens</h2>
          <p>This action permanently cancels the underlying credit to create a measurable impact [4].</p>
          
          <input 
            type="number" 
            placeholder="Amount of MT to offset"
            value={amount} 
            onChange={(e) => setAmount(e.target.value)}
            style={{ padding: '15px', width: '80%', margin: '20px 0', borderRadius: '10px', border: '1px solid #ddd' }}
          />

          <button 
            onClick={handleRetireTransaction}
            disabled={isProcessing}
            style={{
              padding: '15px 40px', 
              background: isProcessing ? '#ccc' : '#2d5a27', 
              color: 'white', 
              border: 'none', 
              borderRadius: '50px', 
              fontWeight: 'bold', 
              cursor: 'pointer',
              width: '100%'
            }}>
            {isProcessing ? "Processing on Blockchain..." : "Confirm & Retire Tokens"}
          </button>
        </div>
      ) : (
        <div style={{ background: '#e8f5e9', padding: '40px', borderRadius: '20px', border: '2px solid #2d5a27' }}>
          <h2 style={{ color: '#2d5a27' }}>Retirement Successful! 📜</h2>
          <p>You have permanently offset <strong>{receipt.amount} Metric Tonnes</strong> of CO2 [5].</p>
          <div style={{ textAlign: 'left', background: 'white', padding: '20px', fontSize: '12px', fontFamily: 'monospace' }}>
            <p><strong>Proof of Impact (NFT ID):</strong> {receipt.id}</p>
            <p><strong>Blockchain TX:</strong> {receipt.txHash}</p>
            <p><strong>Registry Standard:</strong> Verra / Gold Standard [6]</p>
          </div>
          <button onClick={() => setReceipt(null)} style={{ marginTop: '20px', cursor: 'pointer' }}>Retire More</button>
        </div>
      )}
    </div>
  );
}

export default Retire;